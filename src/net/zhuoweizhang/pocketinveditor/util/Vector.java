package net.zhuoweizhang.pocketinveditor.util;

public class Vector {
	private float x, y, z;

	public Vector(float x, float y, float z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY() {
		this.y = y;
	}

	public float getZ() {
		return z;
	}

	public void setZ() {
		this.z = z;
	}
}
